package org.example.gameoanquan.entity.Gem;

public class Gem {
    protected int locate;
    protected int value;

    public int getLocate() {
        return locate;
    }

    public void setLocate(int locate) {
        this.locate = locate;
    }

    public int getPoint() {
        return value;
    }

    public void setPoint(int point) {
        this.value = point;
    }

}
